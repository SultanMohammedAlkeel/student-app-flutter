  static List<Map<String, dynamic>> getQuestionsByCategory(String category) {
    final allData = getFAQData();
    final categoryData = allData.firstWhere(
      (item) => item['category'] == category,
      orElse: () => {'questions': []},
    );
    return List<Map<String, dynamic>>.from(categoryData['questions'] ?? []);
  }

  static List<Map<String, dynamic>> searchQuestions(String query) {
    final allData = getFAQData();
    List<Map<String, dynamic>> results = [];
    
    for (var category in allData) {
      for (var question in category['questions']) {
        if (question['question'].toLowerCase().contains(query.toLowerCase()) ||
            question['answer'].toLowerCase().contains(query.toLowerCase())) {
          results.add({
            ...question,
            'category': category['category'],
          });
        }
      }
    }
    
    return results;
  }
